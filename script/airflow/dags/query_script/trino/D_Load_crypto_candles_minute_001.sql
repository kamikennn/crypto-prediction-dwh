insert into hive.crypto_raw.candles_minute
(
    id,
    low,
    high,
    open,
    close,
    amount,
    quantity,
    buyTakerAmount,
    buyTakerQuantity,
    tradeCount,
    ts,
    weightedAverage,
    interval_type,
    startTime,
    closeTime,
    dt,
    ts_insert_utc,
    year,
    month,
    day,
    hour
)
select
    id,
    low,
    high,
    open,
    close,
    amount,
    quantity,
    buyTakerAmount,
    buyTakerQuantity,
    tradeCount,
    ts,
    weightedAverage,
    interval,
    startTime,
    closeTime,
    dt,
    ts_insert_utc,
    year(from_unixtime(closeTime)),
    month(from_unixtime(closeTime)),
    day(from_unixtime(closeTime)),
    hour(from_unixtime(closeTime))
from cassandra.crypto.candles_minute
where 
    dt > (select COALESCE(max(dt), cast('1111-01-01' as date)) from hive.crypto_raw.candles_minute)